export function td(locale: Locale, key: string, replacements?: Record<string, string | number>): string {
  let text = dashboardTranslations[locale]?.[key] || dashboardTranslations.es[key] || key;
  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
